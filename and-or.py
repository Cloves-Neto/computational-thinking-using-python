#Or e And


a = 5
b = 10

''' O operador and precisa que todas suas estruturas condicionais sejam atendidas para retornar o valor TRUE'''
print(a >= 10 and b == 10 and b > 2)
print(a == 5 and b == 10 and b > 2)

''' JÃ¡ o operador Or precisa que apenas um dos valores sejam atendidos para retornar o valor TRUE'''
print(a == 10 or b == 10)
